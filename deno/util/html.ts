export function* GetExpressions(value: string) {
  let current = "";
  let depth = 0;
  for (const char of value) {
    if (char === "}") {
      depth -= 1;

      if (depth === 0) {
        yield current;
        current = "";
      }
    }

    if (depth > 0) {
      current += char;
    }

    if (char === "{") {
      depth += 1;
    }
  }
}
